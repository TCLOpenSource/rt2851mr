static short EOTFtoPQ_quadrint(short EOETF_base[64], short EOETF_slope[64], short EOETF_bend[64], unsigned short Ei)
{
  unsigned char addr;
  unsigned short delta;
  short Lr, Li;

  addr  = Ei >> 10;
  delta = Ei  & 0x03ff;
  if (!EOETF_bend) {
      Lr = (((short)EOETF_base [(int)addr]) << 2) +
      ((((int)delta) * (((short)EOETF_slope[(int)addr]) << 1)) >> 9);
  }
  else {
    Lr = (((short)EOETF_base [(int)addr]) << 2) +
      ((((int)delta) * ( (((short)EOETF_slope[(int)addr]) << 1) -
      (( ( ((int)1023) - ((int)delta) ) * EOETF_bend[(int)addr]) >> 9) )) >> 9);
  }

  if (Lr >= 0)
    Li = ((Lr  + 2) >> 2);
  else
    Li = -(((-Lr) + 2) >> 2);

  return Li;
}

