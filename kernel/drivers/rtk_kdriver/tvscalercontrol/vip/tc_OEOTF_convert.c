static unsigned short EOTFtoGamma_quadrint(unsigned short EOETF_base[64], unsigned short EOETF_slope[64], unsigned char EOETF_bend[64], unsigned char EOETF_shl[64], unsigned short Ei)
{
  unsigned char addr;
  unsigned short delta;
  int Lr;
  unsigned short Li;

  addr  = Ei >> 10;
  delta = Ei  & 0x03ff;

  Lr = (((unsigned short)EOETF_base[(int)addr]) << 2) +
    ((((int)delta) * ( (((short)EOETF_slope[(int)addr]) << 1) -
    (( ( ((int)1023) - ((int)delta) ) * EOETF_bend[(int)addr]) >> 9) )) >> 9);

  Li = (unsigned short)((Lr > 0) ? ((Lr << ((unsigned char)(EOETF_shl [(int)addr]) << 1)) + 2) >> 2 : 0);

  return Li ;
}

